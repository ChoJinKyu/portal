sap.ui.define([
	"ext/lib/UIComponent",
	"ext/lib/model/models",
	"ext/lib/controller/ErrorHandler",
	"jquery.sap.global",
	"sap/f/FlexibleColumnLayoutSemanticHelper"
], function (UIComponent, models, ErrorHandler, jQuery, FlexibleColumnLayoutSemanticHelper) {
	"use strict";

	return UIComponent.extend("dp.detailSpecEntry.Component", {

		metadata : {
			manifest: "json"
		},

		/**
		 * The component is initialized by UI5 automatically during the startup of the app and calls the init method once.
		 * In this function, the device models are set and the router is initialized.
		 * @public
		 * @override
		 */
		init : function () {
			// call the base component's init function
			UIComponent.prototype.init.apply(this, arguments);
			
			this.setModel(models.createModel(), "fcl");
		},

		/**
		 * Returns an instance of the semantic helper
		 * @returns {sap.f.FlexibleColumnLayoutSemanticHelper} An instance of the semantic helper
		 */
		getHelper: function () {
			var oFCL = this.getRootControl().byId("fcl"),
				oParams = jQuery.sap.getUriParameters(),
				oSettings = {
					defaultTwoColumnLayoutType: sap.f.LayoutType.TwoColumnsMidExpanded,
					//defaultThreeColumnLayoutType: sap.f.LayoutType.ThreeColumnsMidExpanded,
					mode: oParams.get("mode"),
					maxColumnsCount: oParams.get("max")
				};

			return FlexibleColumnLayoutSemanticHelper.getInstanceFor(oFCL, oSettings);
		}

	});

});